# int(s,base)
s="10010"
c=int(s,2)
print(c)
# float
e=float(s)
print(e)

# ord convert a function to character to integer
s='A'
c=ord(s)
print(c)

# hex
a=hex(56)
print(a)

# oct
b=oct(56)
print(b)
# chr   change integer to character
a=chr(97)
print(a)
# dict()
tup = (('a', 1) ,('f', 2), ('g', 3))
c=dict(tup)
print(c)